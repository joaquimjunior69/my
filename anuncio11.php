Por volta de 1992, começaram a se popularizar jogos via [BBS]. Um usuário se conectava a esse serviço e desafiava vários adversários em qualquer lugar do mundo, mas mesmo assim só podia ainda jogar com um deles, aquele que continha a máquina mais rápida. Mas a possibilidade de participar de uma comunidade, bater papo e escolher com quem jogar abriu um novo mundo de diversão. É nessa época que os primeiros jogos online começaram a fazer sucesso e atrair cada vez mais jogadores. Doom (em sua primeira versão), Descent (jogo de naves), F-15E Strike Eagle (jogo de caças) entre outros.
Em 1994 as BBS progrediram e se tornaram grandes serviços nacionais, integrando um grande número de cidades e milhares de jogadores: eram os serviços online como a Compuserve e a America Online.
Internet
Por volta de 1995 as vantagens que a Internet oferecia eram muito maiores e mais atraentes. Alcançe mundial, liberdade de jogar onde quiser e sem pagar. Foi nessa época que surgiram jogos com interfaces adaptadas para jogar em rede e online, como o Doom 2, Descent 2, Heretic, MechAssault, entre outros.
Foi a partir desse ano que os jogos online via rede mundial de computadores se consolidaram e só vieram a crescer se tornando "a diversão do futuro".
Banda Larga
Por volta de 1997, com a chegada das conexões de alta velocidade acessíveis ao público em geral os jogos online viraram coisa séria. Eles agora tinham servidores e comunidades inteiras na rede dedicados só para jogar. Os jogos do tipo FPS se firmaram como os preferidos desse público e deve-se a eles o avanço desses jogos na Internet. Foi nessa época que o primeiro Quake apareceu vindo a se tornar no futuro um sucesso online.
MMORPG
MMORPG (Massive Multiplayer Online Role Playing Game) são jogos do tipo RPG que reúnem multidões de jogadores numa mesma partida. Cada jogador participa de um mundo virtual (frequentemente, com temática medieval) e joga com centenas ou mesmo milhares de outros jogadores. Esses jogos geralmente são pagos mensalmente e costumam oferecer um meio de sociabilização virtual: os personagens se casam, constroem casas, arrumam trabalho, etc.
Jogos por Rede

Jogos por rede são jogos de computador que usam uma rede (internet ou rede local), geralmente através do protocolo TCP/IP, para permitir uma ligação de utilizadores entre si jogos multi-jogadores. No tipo de jogo multiplayer (multi-jogador) podem ser encontrados jogos no estilo de first person shooter como por exemplo o Quake III ou páginas de internet que usam simplesmente a ligação de rede para promover os usuários com sistemas de pontuação.
O jogo do estilo multi-jogador tem chegado à atenção de empresas que patrocinam torneios no mundo pela internet, dando ao jogador a hipotese de se inscrever em jogos pela internet ou em lan party\'s.
O MMOGs e MMORPGs (Massively-multiplayer online games) Jogos online multi-jogador e jogo multi-jogador maciço (massively-multiplayer) em que o jogador desempenha o papel de uma personagem num mundo virtual e vai interagindo com outros utilizadores desse mundo trocando e vendendo objectos virtuais, lutam por terras e tesouros.
